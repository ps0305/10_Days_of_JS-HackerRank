  // Calculate the area of a rectangle.

function getArea(length, width) {
    let area;
    // Write your code here
    area = length * width;
    return area;
}

  // Calculate the perimeter of a rectangle.

function getPerimeter(length, width) {
    let perimeter;
    // Write your code here
    perimeter = 2 * (length + width);
    return perimeter;
}